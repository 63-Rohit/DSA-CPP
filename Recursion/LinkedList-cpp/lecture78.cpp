#include<iostream>
using namespace std;
class Node{
    public:
    int data;
    Node*next;
    // Constructor
    Node(int value)
    {
        data=value;
        next=NULL;
    }
};
int main()
{
    // Static way
    // Node A1(4);

    // Dynamic way
    Node *Head;
    Head=new Node(4);

    cout<<Head->data<<endl;
    cout<<Head->next<<endl;

}


